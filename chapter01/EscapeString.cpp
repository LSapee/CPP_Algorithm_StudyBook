#include <iostream>

using namespace std;

int main(){
    cout<<"Hello\n";
    cout<<"Hi\t friends."<<endl;
    cout<<"Buenos dias  \bamigos." <<endl;
    cout <<"Hello\rBonjour mes amis." <<endl;
    cout<<"This is a single quote\'." <<endl;
    cout<<"This is a double quote\"."<<endl;
    cout<<"This is how to print a backslash \\.";
    /*EscapeString
     * \t 탭문자
     * \b 백스페이스 (한칸 지우기)
     * \r 캐리지 리턴 (커서를 \r뒤쪽으로 이동 즉 aaaa\rbbbb하면 \r보다 앞쪽에 있는 aaaa문자열은 삭제 출력됨)
     * \f 폼피드 (다음페이지로 넘기기)(문서 파일 작성할때 사용)
     * */
    return 0;
}